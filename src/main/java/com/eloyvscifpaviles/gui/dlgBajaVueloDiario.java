    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
       String codigo = cbCodigo.getSelectedItem().toString();
       int dia = Integer.parseInt(txtDia.getText());
       int mes = Integer.parseInt(txtMes.getText());
       int anyo = Integer.parseInt(txtAnyo.getText());
       Date fecha = new Date (anyo - 1901, mes, dia);
       
       VueloDiario borrar = LogicaNegocio.getVueloDiarioByCodigoYFecha(codigo, fecha);
       LogicaNegocio.deleteVueloDiarioByVuelo(borrar);
       LogicaNegocio.actualizarVueloDiarioCSV();
    }//GEN-LAST:event_btnBajaActionPerformed

