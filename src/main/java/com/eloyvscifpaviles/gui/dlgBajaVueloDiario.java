    public void recuperarCodigosVueloBase() {
        List<VueloBase> vuelosBase = LogicaNegocio.leerVuelosBase();

        for (VueloBase v : vuelosBase) {
            cbCodigo.addItem(v.getCodigoVuelo());
        }
        
        cbCodigo.setSelectedIndex(0);
    }
    
